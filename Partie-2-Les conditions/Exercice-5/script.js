let day = Number(prompt("Indiquez un nombre entre 0 et 6"));
switch (day) {
    case 0:
       day = alert("Dimanche");
    break;
    case 1:
       day = alert("Lundi");
    break;
    case 2:
        day = alert("Mardi");
    break;
    case 3:
        day = alert("Mercredi");
    break;
    case 4:
        day = alert("Jeudi");
    break;
    case 5:
        day = alert("Vendredi");
    break;
    case 6:
        day = alert("Samedi");
    break;
}